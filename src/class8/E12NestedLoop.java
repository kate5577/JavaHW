public class E12NestedLoop {
    public static void main(String[] args) {

        for (int i = 0; i < 4; i++) {


            int numb = 2;
            while (numb <= 10) {
                System.out.print(numb + " ");
                numb += 2;
            }

            System.out.println();

        }
    }
}
